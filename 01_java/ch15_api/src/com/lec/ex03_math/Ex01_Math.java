package com.lec.ex03_math;

public class Ex01_Math {
	public static void main(String[] args) {
		System.out.println("2의 3승은"+Math.pow(2,10));
		System.out.println("-9.9의 절대갑은"+Math.abs(-9.9));
		System.out.println("16의 제곱근은"+Math.sqrt(16));
		System.out.println("1,2의 최소값"+Math.min(1,2));
		System.out.println("1,2의 최대값"+Math.max(1,2));
		//Math의 변수(static final 변수
		System.out.println("pi값"+Math.PI);
		System.out.println("sin(PI) :" +Math.sin(Math.PI));
		System.out.println("cos(PI) :" +Math.cos(Math.PI));
		
		//반올림
		
		
	}

}
